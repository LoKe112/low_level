#include <stdio.h>

#define MAX_LEN 100

int main() {
    int arr[MAX_LEN];
    int n;

    // Ввод количества элементов массива
    printf("Введите количество элементов массива (максимум %d): ", MAX_LEN);
    scanf_s("%d", &n);

    // Проверка на корректность ввода
    if (n <= 0 || n > MAX_LEN) {
        printf("Некорректное количество элементов.\n");
        return 1;
    }

    // Ввод элементов массива
    printf("Введите %d элементов массива:\n", n);
    for (int i = 0; i < n; i++) {
        scanf_s("%d", &arr[i]);
    }

    // Сортировка вставками
    for (int i = 1; i < n; i++) {
        int key = arr[i];
        int j;

        // Перемещение элементов, которые больше ключа, на одну позицию вперед
        for (j = i - 1; j >= 0 && arr[j] > key; j--) {
            arr[j + 1] = arr[j];
        }
        arr[j + 1] = key; // Вставка ключа на правильное место
    }

    // Вывод отсортированного массива
    printf("Отсортированный массив:\n");
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");

    return 0;
}